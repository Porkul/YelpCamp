const { campgroundSchema, reviewSchema } = require('./schemas.js');
const ExpressError = require('./utils/ExpressError');
const Campground = require('./models/campground');
const Review = require('./models/review');

//Custom function that we can import and use everywhere within the YelpCamp Directory. isLoggedIn checks if somebody currently signed in.
module.exports.isLoggedIn = (req, res, next) => { // this is a middleware
    if (!req.isAuthenticated()) {
        req.session.returnTo = req.originalUrl //returnTo- we want to redirect user back to 
        req.flash('error', 'You must be signed in first!');
        return res.redirect('/login');
    }
    next(); //orherwise let's go next
}

